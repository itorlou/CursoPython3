# método: función que dará nuevas utilidades o modificará nuestros strings
# vamos a ver los más básicos

cadena = 'Estoy utilizando los metodos de Python'
cadena2 = 'ESTOY UTILIZANDO LOS METODOS DE PYTHON'

# métodos de "caracter"

print (cadena.lower()) #pasamos a minus
print (cadena2.lower()) 

print (cadena.upper())

print (cadena.capitalize()) #muestra solo la primera letra en mayus

print (cadena.title()) #Pasa todas las primeras letras de cada palabra a mayus

print (cadena.swapcase()) #pasa las mayus a minus y las minus a mayus
