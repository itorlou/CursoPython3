# vamos a ver las funciones matemáticas que se pueden hacer para llevar acabo procedimientos matemáticos, están integradas en una librería (o módulo)

import math #librería matemática

print(math.pow(10,2)) #pow eleva un número a un exponente

print(math.sqrt(64)) #sqrt saca la raíz cuadrada (flotante)

print(math.isqrt(64)) #así saca el entero

#trigonométricas
print(math.sin(90)) #seno

print(math.cos(90)) #coseno

print(math.tan(90)) #tangente

#factorial
print(math.factorial(5)) 

import random #librería random

print(random.randint(1,100)) #saca un número aleatorio entre el rango proporcionado




