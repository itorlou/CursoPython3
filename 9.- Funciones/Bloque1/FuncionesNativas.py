# vamos a ver qué funciones son nativas en python

num= '70'

print(type(num)) #esto "type" es una función que devuelve el tipo de dato

print(type(int(num))) # la función "int" convierte un dato a entero

print(type(float(num))) # convierte a un float

lista = [ 12, 60, 70, 1, -2]

# a esta lista podemos aplicar funciones

print(len(lista)) #funcion que nos devuelve la cantidad de valores que hay en la lista

print(max(lista)) #devuelve el valor máximo de la lista